package com.hitomiDownlaoder.service.login;

import com.fasterxml.jackson.core.JsonParser;
import com.fasterxml.jackson.core.type.TypeReference;
import com.fasterxml.jackson.databind.ObjectMapper;
import org.apache.tomcat.util.json.JSONParser;
import org.springframework.stereotype.Service;

import java.io.*;
import java.net.HttpURLConnection;
import java.net.MalformedURLException;
import java.net.ProtocolException;
import java.net.URL;
import java.util.Map;

@Service
public class LoginServiceImpl implements LoginService {

    @Override
    public String getAccessToken (String authorizeCode) {
        String access_Token = "";
        String refresh_Token = "";
        String reqURL = LoginConstants.KAKAO_TOKEN_URL;
        Map<String, Object> resultMap = null;

        try {
            URL url = new URL(reqURL);

            HttpURLConnection conn = (HttpURLConnection) url.openConnection();

            //    POST 요청을 위해 기본값이 false인 setDoOutput을 true로

            conn.setRequestMethod("POST");
            conn.setDoOutput(true);

            //    POST 요청에 필요로 요구하는 파라미터 스트림을 통해 전송
            BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(conn.getOutputStream()));
            StringBuilder sb = new StringBuilder();
            sb.append("grant_type=authorization_code");
            sb.append("&client_id=" + LoginConstants.CLIENT_ID);  //본인이 발급받은 key
            sb.append("&redirect_uri=" + LoginConstants.REDIRECT_URL);     // 본인이 설정해 놓은 경로
            sb.append("&code=" + authorizeCode);
            bw.write(sb.toString());
            bw.flush();

            //    결과 코드가 200이라면 성공
            int responseCode = conn.getResponseCode();
            System.out.println("responseCode : " + responseCode);

            //    요청을 통해 얻은 JSON타입의 Response 메세지 읽어오기
            BufferedReader br = new BufferedReader(new InputStreamReader(conn.getInputStream()));
            String line = "";
            String result = "";

            while ((line = br.readLine()) != null) {
                result += line;
            }
            System.out.println("response body : " + result);

            //    Gson 라이브러리에 포함된 클래스로 JSON파싱 객체 생성
            ObjectMapper mapper = new ObjectMapper();
            resultMap = mapper.readValue(result, new TypeReference<Map<String,Object>>() {});

            access_Token = (String) resultMap.get("access_token");
            refresh_Token = (String) resultMap.get("refresh_token");

            System.out.println("access_token : " + access_Token);
            System.out.println("refresh_token : " + refresh_Token);

            br.close();
            bw.close();
        } catch (IOException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }

        return access_Token;
    }
}
